        <p>I’ve gone back and forth about the utility of social media companies. They connect people and create opportunities otherwise impossible to access. On the other hand, they have created a host of negative side effects that leave us all a bit worse off for having used them. Are these companies, on balance, helpful or harmful? The best thing I’ve come up with so far is: both.</p>


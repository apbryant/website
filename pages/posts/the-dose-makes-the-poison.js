<p>For the laws and institutions to change, the culture has to shift first. Culture is upstream of everything else in a society. If you change the laws but not the culture, eventually the laws will change right back. For a sustainable business ecosystem, a culture of “enough” needs to be cultivated. We need people who want to do good work for fair compensation. People who understand that the biggest reward for work is the process itself, and the fruits of domination have a bitter aftertaste. We need people who don’t confuse numbers in a bank account with real wealth; the skills, relationships, and tangible assets that provide for you and yours.</p>


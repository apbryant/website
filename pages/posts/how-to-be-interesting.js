        <p>If I were giving job-seeking advice to my college-age self, or to someone in college looking for their first job, I’d tell them to not spend so much time on the traditional job-search methods of résumés and cold-emails. Instead, just be as interesting of a person as you can. One of the best ways to send opportunities your way is to be an interesting person. If you’re an interesting person who’s always doing something, who’s always got something going on, people will naturally send stuff your way so you can be a part of their lives. People like being around people who are going places. Don’t worry too much about your GPA. Well, don’t fail out. But if you don’t have a 4.0, don’t stress about it. One of my favorite quotes from Chris Sacca is: “your GPA only matters to people who have no other reason to find you interesting.” If you’re interesting person, you’ll get a decent job anyway.</p>

